"""
7) Fazer um algoritmo que pergunte 3 números e apresente a média aritmética entre estes 3 números

"""
nota1 = float(input("Informe a sua nota 1"))
nota2 = float(input("Informe a sua nota 2"))
nota3 = float(input("Informe a sua nota 3"))

média = (nota1 + nota2 + nota3) / 3

print("Sua média é", média)
